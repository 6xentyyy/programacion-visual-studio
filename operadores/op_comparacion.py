variable_1 = 6

# Operador EQUIVALENTE A
print("Operador EQUIVALENTE A")
print(variable_1 == 6)
print(variable_1 == 8)

# Operador DISTINTO DE 
print("Operador DISTINTO DE")
print(variable_1 != 6)
print(variable_1 != 8)

# Operador MAYOR QUE 
print("Operador MAYOR QUE")
print(variable_1 > 6)
print(variable_1 > 8)

# Operador MAYOR O IGUAL QUE
print("Operador MAYOR O IGUAL QUE")
print(variable_1 >= 4)
print(variable_1 >= 8)

# Operador MENOR QUE
print("Operador MENOR QUE")
print(variable_1 < 4) 
print(variable_1 < 8)

# Operador MENOR O IGUAL QUE 
print("Operador MENOR O IGUAL QUE")
print(variable_1 <= 3)
print(variable_1 <= 8)